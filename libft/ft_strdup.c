#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*c;
	size_t	i;

	i = 0;
	while (s1[i] != '\0')
		i++;
	c = malloc(sizeof(*s1) * (i + 1));
	if (!c)
		return (NULL);
	i = 0;
	while (s1[i] != '\0')
	{
		c[i] = s1[i];
		i++;
	}
	c[i] = '\0';
	return (c);
}
