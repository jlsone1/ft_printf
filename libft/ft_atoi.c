int	ft_atoi(const char *str)
{
	int	i;
	int	p;
	int	j;

	i = 0;
	p = 1;
	j = 0;
	while (str[i] == '\t' || str[i] == '\n' || str[i] == '\n'
		|| str[i] == '\f' || str[i] == '\r' || str[i] == ' '
		||str[i] == '\v')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			p = p * -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		j = 10 * j + str[i] - '0';
		i++;
	}
	return (p * j);
}
