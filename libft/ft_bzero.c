#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned long	i;
	unsigned char	*a;

	i = 0;
	a = (unsigned char *)s;
	if (n != 0)
	{
		while (i < n)
		{
			a[i] = '\0';
			i++;
		}
	}
}
